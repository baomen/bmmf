
namespace BaoMen.Common.Constant
{
    /// <summary>
    /// 时间格式化串
    /// </summary>
    public static class DateTimeFormat
    {
        /// <summary>
        /// 长日期。从年到毫秒
        /// </summary>
        public const string LongYearToMillisecond = "yyyy-MM-dd HH:mm:ss.fff";

        /// <summary>
        /// 长日期。从年到秒
        /// </summary>
        public const string LongYearToSecond = "yyyy-MM-dd HH:mm:ss";

        /// <summary>
        /// 长日期。从年到天
        /// </summary>
        public const string LongYearToDay = "yyyy-MM-dd";

        /// <summary>
        /// 长日期。从小时到秒
        /// </summary>
        public const string LongHourToSecond = "HH:mm:ss";

        /// <summary>
        /// 短日期。从年到秒。
        /// </summary>
        public const string ShortYearToSecond = "yyyy-M-d H:m:s";

        /// <summary>
        /// 短日期。从年到天。
        /// </summary>
        public const string ShortYearToDay = "yyyy-M-d";

        /// <summary>
        /// 短日期。从小时到秒
        /// </summary>
        public const string ShortHourToSecond = "h:m:s";

        /// <summary>
        /// 14个字符的时间日期格式
        /// </summary>
        public const string Character14 = "yyyyMMddHHmmss";

        /// <summary>
        /// 17个字符的时间日期格式
        /// </summary>
        public const string Character17 = "yyyyMMddHHmmssfff";
    }
}
